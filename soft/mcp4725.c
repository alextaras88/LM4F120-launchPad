#include "mcp4725.h"

/******************************************************************************/
/* MCP4725 12 bit DAC  i2c                                                    */
/******************************************************************************/
void Write_mcp4725(uint8_t addr, uint16_t data, uint8_t config){
  
  i2c_addr(addr,0);
  i2c_write_data(config,(1<<0)|(1<<1));
  i2c_write_data((uint8_t)(data/16), (1<<0));
  i2c_write_data((uint8_t)(data %16)<<4, (1<<0)|(1<<2));
  
}
